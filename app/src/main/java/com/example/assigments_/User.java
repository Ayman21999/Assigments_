package com.example.assigments_;

public class User {

    String username;
    String address;
    String number;
    public String getusername() {
        return username;
    }

    public void setusername(String name) {
        this.username = name;
    }

    public String getNumber() {
        return number;
    }

    public void setNumber(String usernnumber) {
        this.number = usernnumber;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String addres) {
        this.address = addres;
    }
}
